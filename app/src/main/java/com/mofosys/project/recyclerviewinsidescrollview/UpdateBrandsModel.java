public class UpdateBrandsModel {

    private String image;
    private String my_brand_name;
    private String my_brand_id;
    boolean checkbox;

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getMy_brand_name() {
        return my_brand_name;
    }

    public void setMy_brand_name(String my_brand_name) {
        this.my_brand_name = my_brand_name;
    }

    public String getMy_brand_id() {
        return my_brand_id;
    }

    public void setMy_brand_id(String my_brand_id) {
        this.my_brand_id = my_brand_id;
    }

    public boolean isCheckbox() {
        return checkbox;
    }

    public void setCheckbox(boolean checkbox) {
        this.checkbox = checkbox;
    }

}
